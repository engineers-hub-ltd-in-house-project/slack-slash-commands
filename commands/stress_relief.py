import random

def get_stress_relief_message():
    """
    ストレス解消メッセージをランダムに返す
    """
    messages = [
        "深呼吸をして、肩の力を抜いてみましょう。今日も一日頑張りましたね。",
        "あなたは十分に頑張っています。小さな成功も自分を褒めてあげましょう。",
        "一時的に目を閉じて、好きな場所を想像してみてください。心が落ち着きます。",
        "水を一杯飲んで、短い散歩をしてみてはいかがでしょうか？",
        "心配事は紙に書き出すと、整理されて気持ちが楽になります。",
        "あなたの頑張りは必ず実を結びます。少し休憩して、また前に進みましょう。",
        "自分にとって小さな幸せを見つけてみましょう。コーヒーの香り、窓から見える空など。",
        "「今」この瞬間に集中してみましょう。過去や未来の心配から離れられます。",
        "自分の感情に名前をつけてみましょう。「今、焦りを感じているんだな」と認識するだけでも楽になります。",
        "今日の小さな成功や感謝できることを3つ挙げてみましょう。"
    ]
    
    return random.choice(messages)

def get_mindful_moment():
    """
    マインドフルネスのプロンプトをランダムに返す
    """
    prompts = [
        "今、あなたの周りで聞こえる3つの音に意識を向けてみてください。",
        "今、感じている体の感覚に意識を向けてみましょう。",
        "今日、あなたが感謝できることは何ですか？",
        "窓の外を1分間見つめてみてください。何か新しい発見はありますか？",
        "今この瞬間、あなたの呼吸に意識を向けてみてください。",
        "あなたの周りにある5つの物を観察してみてください。色や形に注目します。",
        "一口の食べ物や飲み物を、味、香り、食感に注意を払いながらゆっくり味わってみましょう。",
        "今日見た美しいものは何ですか？それについて30秒考えてみましょう。"
    ]
    
    return random.choice(prompts)
